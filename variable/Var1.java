public class Var1{
  public static void main(String[] args){
    int grades = 20;
    if(grades >= 90)
      System.out.println("A");
    else if(grades >= 80)
      System.out.println("B");
    else if(grades >= 70)
      System.out.println("C");
    else if(grades >= 60)
      System.out.println("D");
    else
      System.out.println("F");
  }
}